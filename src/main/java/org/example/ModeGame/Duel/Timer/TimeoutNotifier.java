package org.example.ModeGame.Duel.Timer;

/**
 * Интерфейс для получения уведомлений о таймаутах дуэлей и поиска оппонентов.
 * Реализуется классами, которые должны обрабатывать события истечения времени ожидания.
 */
public interface TimeoutNotifier {

    /**
     * Вызывается при истечении времени поиска оппонента.
     * Используется для отправки сообщения игроку о том, что поиск отменен по таймауту.
     *
     * @param chatId уникальный идентификатор чата игрока, чей поиск истек
     * @param topic тема, по которой велся поиск (может быть "неизвестная тема" в случае ошибки)
     */
    void notifySearchTimeout(String chatId, String topic);

    /**
     * Вызывается при истечении времени ожидания второго игрока в дуэли.
     * Используется для завершения дуэли по таймауту и определения победителя.
     *
     * @param duelId уникальный идентификатор дуэли
     * @param player1ChatId идентификатор чата первого игрока (уже завершил дуэль)
     * @param player2ChatId идентификатор чата второго игрока (не успел завершить дуэль)
     */
    void notifyDuelTimeout(String duelId, String player1ChatId, String player2ChatId);
}